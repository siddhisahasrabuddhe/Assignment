package day4;

public final class swapping {

	public static void main(String[] args) {
		int a=10, b=20, temp;
		
		System.out.println("before swapping");
		System.out.println("Valus of a "+a+" Value of b "+b);
		
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("after swapping");
		System.out.println("Valus of a "+a+" Value of b "+b);
		

	}

}
